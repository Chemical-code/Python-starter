# Declare the list
list_a = [1, 2, 3]
list_b = [4, 5, 6]

# Print
print("# List")
print("list_a =", list_a)
print("list_b =", list_b)
print()

# Basic operator
print("# List basic operator")
print("list_a + list_b", list_a + list_b)
print("list_a * 3 =", list_a * 3)
print()

# Function
print("# Length")
print("len(list_a) =", len(list_a))
