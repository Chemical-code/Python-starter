# Integer
output_a = "{:d}".format(52)

# Print certain location
output_b = "{:5d}".format(52)
output_c = "{:10d}".format(52)

# Fill blank with 0
output_d = "{:05d}".format(52)
output_e = "{:05d}".format(-52)

print("# Basic")
print(output_a)
print("# Print certain location")
print(output_b)
print(output_c)
print("# Fill blank with 0")
print(output_d)
print(output_e)